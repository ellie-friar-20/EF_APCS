public class SimpleCounterButAForLoop
{
    public static void main(String[] args)
    {
        // learning how to use while loop for imple tasks such as counting//
        int i = 0;
        while (i < 300)
        {
            System.out.println(i);
            i++;
        }
        while (i > 0)
        {
            System.out.println(i);
            i--;
        }
        int y = 4798;
        while (y < 5324)
        {
            System.out.println(y);
            y++;
        }
        while (y > 4797)
        {
            System.out.println(y);
            y--;
        }
    }
}