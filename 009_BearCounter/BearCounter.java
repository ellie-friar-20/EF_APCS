public class BearCounter
{
    public static void main(String[] args) 
    {
        //using increment and decrement operator to change an int.// 
        int bearCount = 0;
        bearCount++;
        bearCount++;
        bearCount++;
        System.out.println("Oh no, here come all" + " " + bearCount + " " + "bears!");
        --bearCount;
        --bearCount;
        --bearCount;
        System.out.println("Thank goodness, there is " + " " + bearCount + " " + "bears now.");
    }


}